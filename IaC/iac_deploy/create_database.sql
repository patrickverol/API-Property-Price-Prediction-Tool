CREATE TABLE IF NOT EXISTS especificacoes_casa (
    id SERIAL PRIMARY KEY,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,  -- Column to store the timestamp when the data is input
    host_name VARCHAR(30),
    estimated_price FLOAT,
    host_listings_count INTEGER NOT NULL,
    latitude FLOAT NOT NULL,
    longitude FLOAT NOT NULL,
    accommodates INTEGER NOT NULL,
    bathrooms INTEGER NOT NULL,
    bedrooms INTEGER NOT NULL,
    beds INTEGER NOT NULL,
    extra_people INTEGER NOT NULL,
    minimum_nights INTEGER NOT NULL,
    n_amenities INTEGER NOT NULL,
    room_type_Entire_home_apt INTEGER NOT NULL CHECK (room_type_Entire_home_apt IN (0, 1)),
    host_is_superhost INTEGER DEFAULT 0 CHECK (host_is_superhost IN (0, 1)),
    instant_bookable INTEGER DEFAULT 1 CHECK (instant_bookable IN (0, 1)),
    ano INTEGER DEFAULT EXTRACT(YEAR FROM CURRENT_DATE),
    mes INTEGER DEFAULT EXTRACT(MONTH FROM CURRENT_DATE),
    property_type_Apartment INTEGER DEFAULT 0 CHECK (property_type_Apartment IN (0, 1)),
    property_type_Bed_and_breakfast INTEGER DEFAULT 0 CHECK (property_type_Bed_and_breakfast IN (0, 1)),
    property_type_Condominium INTEGER DEFAULT 0 CHECK (property_type_Condominium IN (0, 1)),
	property_type_Guest_suite INTEGER DEFAULT 0 CHECK (property_type_Guest_suite IN (0, 1)),
    property_type_Guesthouse INTEGER DEFAULT 0 CHECK (property_type_Guesthouse IN (0, 1)),
    property_type_Hostel INTEGER DEFAULT 0 CHECK (property_type_Hostel IN (0, 1)),
    property_type_House INTEGER DEFAULT 0 CHECK (property_type_House IN (0, 1)),
    property_type_Loft INTEGER DEFAULT 0 CHECK (property_type_Loft IN (0, 1)),
    property_type_Outros INTEGER DEFAULT 0 CHECK (property_type_Outros IN (0, 1)),
    property_type_Serviced_apartment INTEGER DEFAULT 0 CHECK (property_type_Serviced_apartment IN (0, 1)),
    room_type_Hotel_room INTEGER DEFAULT 0 CHECK (room_type_Hotel_room IN (0, 1)),
    room_type_Private_room INTEGER DEFAULT 0 CHECK (room_type_Private_room IN (0, 1)),
    room_type_Shared_room INTEGER DEFAULT 0 CHECK (room_type_Shared_room IN (0, 1)),
    cancellation_policy_flexible INTEGER DEFAULT 0 CHECK (cancellation_policy_flexible IN (0, 1)),
    cancellation_policy_moderate INTEGER DEFAULT 0 CHECK (cancellation_policy_moderate IN (0, 1)),
    cancellation_policy_strict INTEGER DEFAULT 0 CHECK (cancellation_policy_strict IN (0, 1)),
    cancellation_policy_strict_14_with_grace_period INTEGER DEFAULT 0 CHECK (cancellation_policy_strict_14_with_grace_period IN (0, 1))
);

